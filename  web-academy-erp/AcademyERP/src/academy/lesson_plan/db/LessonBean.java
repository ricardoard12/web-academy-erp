package academy.lesson_plan.db;

public class LessonBean {

	private int lesson_num;
	private String lesson_subject;
	private String lesson_teacher;
	private String lesson_goal;
	private String lesson_book;
	private String lesson_cost;
	private String lesson_time;
	private String lesson_content;
	private String lesson_date;
	
	public int getLesson_num() {
		return lesson_num;
	}
	public void setLesson_num(int lesson_num) {
		this.lesson_num = lesson_num;
	}
	public String getLesson_subject() {
		return lesson_subject;
	}
	public void setLesson_subject(String lesson_subject) {
		this.lesson_subject = lesson_subject;
	}
	public String getLesson_teacher() {
		return lesson_teacher;
	}
	public void setLesson_teacher(String lesson_teacher) {
		this.lesson_teacher = lesson_teacher;
	}
	public String getLesson_goal() {
		return lesson_goal;
	}
	public void setLesson_goal(String lesson_goal) {
		this.lesson_goal = lesson_goal;
	}
	public String getLesson_book() {
		return lesson_book;
	}
	public void setLesson_book(String lesson_book) {
		this.lesson_book = lesson_book;
	}
	public String getLesson_cost() {
		return lesson_cost;
	}
	public void setLesson_cost(String lesson_cost) {
		this.lesson_cost = lesson_cost;
	}
	public String getLesson_time() {
		return lesson_time;
	}
	public void setLesson_time(String lesson_time) {
		this.lesson_time = lesson_time;
	}
	public String getLesson_content() {
		return lesson_content;
	}
	public void setLesson_content(String lesson_content) {
		this.lesson_content = lesson_content;
	}
	public String getLesson_date() {
		return lesson_date;
	}
	public void setLesson_date(String lesson_date) {
		this.lesson_date = lesson_date;
	}
	
}
